package cl.model;

public class TeacherInfo {
	
	private int te_num;
	private String te_name;
	private String te_id;
	private String te_password;
	private String te_tel;
	private String te_address;
	
	public int getTe_num(){
		return te_num;
	}
	
	public void setTe_num(int te_num){
		this.te_num = te_num;
	}
	
	public String getTe_name(){
		return te_name;
	}
	
	public void setTe_name(String te_name){
		this.te_name = te_name;
	}
	
	public String getTe_id(){
		return te_id;
	}
	
	public void setTe_id(String te_id){
		this.te_id = te_id;
	}
	
	public String getTe_password(){
		return te_password;
	}
	
	public void setTe_password(String te_password){
		this.te_password = te_password;
	}
	
	public String getTe_tel(){
		return te_tel;
	}
	
	public void setTe_tel(String te_tel){
		this.te_tel = te_tel;
	}
	
	public String getTe_address(){
		return te_address;
	}
	
	public void setTe_address(String te_address){
		this.te_address = te_address;
	}
	
}
